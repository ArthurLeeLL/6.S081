uint64
sys_sysinfo(void)
{
    struct proc* myProc = myproc();
    struct sysinfo* sinfo;
    struct sysinfo s;

    sinfo = &s;
    sinfo->freemem = allFreeMemory();
    sinfo->nproc = allProcessesNumber();
    uint64 st;
    argaddr(0, &st);
    if(copyout(myProc->pagetable, st, (char *)sinfo, sizeof(s)) < 0)
        return -1;
    return 0;
}